// Build a single, dependency-free dist/avatar-face.js that any project can use
// with a plain <script> tag (no bundler / module system required):
//
//   <script src="avatar-face.js"></script>
//   <avatar-face seed="aoi" state="idle"></avatar-face>
//   // or programmatically: AvatarFace.renderFace(AvatarFace.generate('aoi'))
//
// Everything here is pure ESM with no external deps, so "bundling" is just
// concatenating the modules (imports/exports stripped) inside one IIFE.

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const read = (p) => readFileSync(join(root, p), 'utf8');

// strip ESM import lines and the `export ` keyword (keeps declarations top-level)
function strip(src) {
  return src
    .replace(/^\s*import\s+[^;]*;\s*$/gm, '')
    .replace(/^\s*export\s+(?=(const|function|class|let|var)\b)/gm, '');
}

// concatenation order = dependency order
const modules = [
  'src/genome/color.js',
  'src/genome/rng.js',
  'src/genome/palette.js',
  'src/render/proportions.js',
  'src/render/parts.js',
  'src/genome/generator.js',
  'src/render/face.js',
  'src/anim/idle.js',
];

let code = modules
  .map((p) => {
    let s = strip(read(p));
    // face.js calls parts.* — after concatenation those are top-level
    if (p.endsWith('face.js')) s = s.replace(/\bparts\./g, '');
    return `// ===== ${p} =====\n${s}`;
  })
  .join('\n\n');

const css = read('src/anim/avatar.css');
const cssLiteral = JSON.stringify(css);

const component = `
// ===== <avatar-face> custom element =====
const AV_CSS = ${cssLiteral};
const HOST_CSS = ':host{display:inline-block;width:120px;height:120px;line-height:0}svg{width:100%;height:100%;display:block}';
let __uidc = 0;
class AvatarFace extends HTMLElement {
  static get observedAttributes() { return ['seed', 'fem', 'age', 'bg-sat', 'state', 'animated']; }
  connectedCallback() { if (!this.shadowRoot) this.attachShadow({ mode: 'open' }); this._render(); }
  disconnectedCallback() { if (this._idle) this._idle.destroy(); }
  attributeChangedCallback(name, _o, val) {
    if (!this.shadowRoot || !this.shadowRoot.firstChild) return;
    if (name === 'state' && this._idle) { this._idle.setState(val || 'idle'); return; }
    this._render();
  }
  set state(v) { this.setAttribute('state', v); }
  get state() { return this.getAttribute('state') || 'idle'; }
  _render() {
    const seed = this.getAttribute('seed') || 'avatar';
    const opts = {};
    const femAttr = this.getAttribute('fem');
    if (femAttr != null && femAttr !== '') opts.fem = parseFloat(femAttr);
    const ageAttr = this.getAttribute('age');
    if (ageAttr != null && ageAttr !== '') opts.age = parseFloat(ageAttr);
    const satAttr = this.getAttribute('bg-sat');
    if (satAttr != null && satAttr !== '') opts.bgSat = parseFloat(satAttr);
    const g = generate(seed, opts);
    const svg = renderFace(g, 'ac' + __uidc++);
    if (this._idle) { this._idle.destroy(); this._idle = null; }
    this.shadowRoot.innerHTML = '<style>' + AV_CSS + HOST_CSS + '</style>' + svg;
    if (this.getAttribute('animated') !== 'false') {
      this._idle = createIdle(this.shadowRoot.querySelector('svg'));
      this._idle.setState(this.getAttribute('state') || 'idle');
    }
  }
}
if (typeof customElements !== 'undefined' && !customElements.get('avatar-face')) {
  customElements.define('avatar-face', AvatarFace);
}
if (typeof window !== 'undefined') {
  window.AvatarFace = { generate, renderFace, createIdle, element: AvatarFace, version: '0.0.1' };
}
`;

const out = `/* 2D Light Avatar Maker — single-file build. Generated by tools/bundle.mjs. */\n(function () {\n'use strict';\n${code}\n${component}\n})();\n`;

mkdirSync(join(root, 'dist'), { recursive: true });
writeFileSync(join(root, 'dist', 'avatar-face.js'), out);
console.log('wrote dist/avatar-face.js (' + (out.length / 1024).toFixed(1) + ' KB)');
